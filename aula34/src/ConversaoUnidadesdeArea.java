public class ConversaoUnidadesdeArea {

    public static double metrosQuadradosPesQuadrados(double n1){
        return n1 * 10.76;
    }

    public static double pesQuadradosMetrosQuadrados(double n1){
        return n1/10.76;
    }

    public static double pesQuadradosCentimetrosQuadrados(double n1){
        return n1 * 929;
    }

    public static double centimetrosQuadradosPesQuadrados(double n1){
        return n1 / 929;
    }

    public static double milhasQuadradasAcres(double n1){
        return n1 * 640;
    }

    public static double acresMilhasQuadradas(double n1){
        return n1 / 640;
    }

    public static double acresPesQuadrados(double n1){
        return n1 * 43560;
    }

    public static double pesQuadradosAcres(double n1){
        return n1 / 43560;
    }


}
