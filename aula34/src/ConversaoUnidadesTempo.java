public class ConversaoUnidadesTempo {
    public static double minutosSegundos(double n1){
        return n1 * 60;
    }

    public static double segundosMinutos(double n1){
        return n1 / 60;
    }

    public static double horaMinutos(double n1){
        return n1 * 60;
    }

    public static double minutosHoras(double n1){
        return n1 / 60;
    }

    public static double diasHoras(double n1){
        return n1 * 24;
    }

    public static double horasDias(double n1){
        return n1 / 24;
    }

    public static double semanaDias(double n1){
        return n1 * 7;
    }

    public static double diasSemanas(double n1){
        return n1 / 7;
    }

    public static double mesDias(double n1){
        return n1 * 30;
    }

    public static double diasMes(double n1){
        return n1 / 30;
    }

    public static double anoDias(double n1){
        return n1 * 365;
    }

    public static double diasAno(double n1){
        return n1 / 365;
    }



}
