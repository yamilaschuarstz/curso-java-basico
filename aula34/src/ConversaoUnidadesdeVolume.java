public class ConversaoUnidadesdeVolume {
    public static double litrosCentimetrosCubicos(double n1){
        return n1 * 1000;
    }

    public static double centimetrosCubicosLitros(double n1){
        return n1 / 1000;
    }

    public static double metrosCubicosLitros(double n1){
        return n1 * 1000;
    }

    public static double litrosMetrosCubicos(double n1){
        return n1 / 1000;
    }

    public static double metrosCubicosPesCubicos(double n1){
        return n1 * 35.32;
    }

    public static double pesCubicosMetros(double n1){
        return n1 / 35.32;
    }

    public static double galaoAmericanoPolegadasCubicas(double n1){
        return n1 * 231;
    }

    public static double polegadasCubicasGalaoAmericano(double n1){
        return n1 / 231;
    }

    public static double galaoAmericanoLitros(double n1){
        return n1 * 3785;
    }

    public static double litrosGalaoAmericano(double n1){
        return n1 / 3785;
    }
}
